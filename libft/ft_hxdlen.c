#include "libft.h"

size_t	ft_hxdlen(long n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		if (n < -9223372036854775807)
			return (17);
		n = ft_abs(n);
		i++;
	}
	while (n > 0)
	{
		n = n / 16;
		i++;
	}
	return (i);
}
